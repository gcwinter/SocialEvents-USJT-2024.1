    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        if (this.passForm.getText().equals(this.RepeatPassForm.getText())) {

            User newUser = new User(false,
                    this.loginForm.getText(),
                    new String(this.passForm.getPassword()),
                    this.nameForm.getText(),
                    Integer.parseInt(this.ageForm.getText()),
                     this.genderForm.getText(),
                     this.emailForm.getText(),
                     this.cpfForm.getText()
            );

            UserDAO userDAO = new UserDAO();
            try {
                userDAO.create(newUser);
                if(userDAO.exist(newUser)){
                    JOptionPane.showMessageDialog(null, "Usu√°rio Criado Com Sucesso");
                }
            } catch (Exception e) {
                System.out.println(e);
            }
            
        }else{
                JOptionPane.showMessageDialog(null, "Usuario nao criado");}


    }//GEN-LAST:event_createBtnActionPerformed

